package dto;

public class Car {
	private String carName;		// 모델명
	private int carCost;		// 가격
	private String carFuel;		// 연료
	private String carYear;		// 출시년도
	private String carPower;		// 출력
	
	public Car() {
		
	}

	public String getCarName() {
		return carName;
	}

	public void setCarName(String carName) {
		this.carName = carName;
	}

	public int getCarCost() {
		return carCost;
	}

	public void setCarCost(int carCost) {
		this.carCost = carCost;
	}

	public String getCarFuel() {
		return carFuel;
	}

	public void setCarFuel(String carFuel) {
		this.carFuel = carFuel;
	}

	public String getCarYear() {
		return carYear;
	}

	public void setCarYear(String carYear) {
		this.carYear = carYear;
	}

	public String getCarPower() {
		return carPower;
	}

	public void setCarPower(String carPower) {
		this.carPower = carPower;
	}


	
	
}
